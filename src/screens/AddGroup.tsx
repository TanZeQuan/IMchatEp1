import { colors, borders, typography } from "../styles";
import { useNavigation, useFocusEffect } from "@react-navigation/native";
import { NativeStackNavigationProp } from "@react-navigation/native-stack";
import { MainStackParamList } from "../navigation/MainStack";
import { useUserStore } from "../store/userStore";
import { getFriendRequests } from "../api/FriendApi";
import { addGroup } from "../api/Group";

interface Contact {
  id: string;
  name: string;
  userId: string;
  image?: string;
}

const FriendItem = ({
  item,
  isSelected,
  onToggle,
}: {
  item: Contact;
  isSelected: boolean;
  onToggle: (id: string) => void;
}) => {
  return (
    <TouchableOpacity
      activeOpacity={0.7}
      onPress={() => onToggle(item.id)}
      style={styles.friendItem}
    >
      <View style={styles.leftSection}>
        <View style={styles.avatarPlaceholder}>
          <Image
            source={{ uri: item.image || "https://postimg.cc/34y84VvN" }}
            style={styles.avatarImage}
          />
        </View>
        <View>
          <Text style={styles.friendName}>{item.name}</Text>
          <Text style={styles.friendId}>ID: {item.userId}</Text>
        </View>
      </View>

      <View style={styles.rightSection}>
        <View
          style={[
            styles.checkbox,
            isSelected && styles.checkboxSelected,
          ]}
        >
          {isSelected && (
            <Ionicons name="checkmark" size={18} color={colors.background.white} />
          )}
        </View>
      </View>
    </TouchableOpacity>
  );
};

export default function AddGroup() {
  const navigation = useNavigation<NativeStackNavigationProp<MainStackParamList>>();
  const { userId } = useUserStore();
  const [searchText, setSearchText] = useState("");
  const [selectedFriends, setSelectedFriends] = useState<string[]>([]);
  const [contacts, setContacts] = useState<Contact[]>([]);
  const [isLoading, setIsLoading] = useState(true);
  const [isCreating, setIsCreating] = useState(false);
  const [groupName, setGroupName] = useState("");

  // Fetch friends when component mounts
  useFocusEffect(
    React.useCallback(() => {
      if (userId) {
        fetchFriends();
      }
    }, [userId])
  );

  const fetchFriends = async () => {
    if (!userId) {
      console.log('‚ö†Ô∏è No userId available');
      setIsLoading(false);
      return;
    }

    try {
      setIsLoading(true);
      const response = await getFriendRequests(userId, 2); // Get accepted friends (status=2)

      if (response && !response.error && response.response) {
        const requestFriends = response.response.request || [];
        const approveFriends = response.response.approve || [];

        const allFriends: Contact[] = [];

        // Friends where I sent the request
        requestFriends.forEach((friend: any) => {
          const friendUserId = friend.approve_id || friend.user_id || friend.id;
          allFriends.push({
            id: friend.list_id || friendUserId || String(Math.random()),
            name: friend.name || friendUserId || "Êú™Áü•Áî®Êà∑",
            userId: friendUserId,
            image: friend.image,
          });
        });

        // Friends where I received the request
        approveFriends.forEach((friend: any) => {
          const friendUserId = friend.request_id || friend.user_id || friend.id;
          allFriends.push({
            id: friend.list_id || friendUserId || String(Math.random()),
            name: friend.name || friendUserId || "Êú™Áü•Áî®Êà∑",
            userId: friendUserId,
            image: friend.image,
          });
        });

        console.log('‚úÖ Loaded friends for group:', allFriends.length);
        setContacts(allFriends);
      }
    } catch (error) {
      console.error('‚ùå Error fetching friends:', error);
      Alert.alert('ÈîôËØØ', 'Êó†Ê≥ïÂä†ËΩΩÂ•ΩÂèãÂàóË°®');
    } finally {
      setIsLoading(false);
    }
  };

  const toggleFriend = (id: string) => {
    setSelectedFriends((prev) => {
      const newSelected = prev.includes(id) ? prev.filter((fid) => fid !== id) : [...prev, id];

      // Auto-generate group name when selection changes
      if (newSelected.length > 0) {
        const selectedContacts = contacts.filter((contact) => newSelected.includes(contact.id));
        const autoGeneratedName = selectedContacts
          .map((contact) => contact.name)
          .slice(0, 3)
          .join("„ÄÅ") + (selectedContacts.length > 3 ? "..." : "");

        // Only update if user hasn't manually edited the name
        if (!groupName || prev.length === 0) {
          setGroupName(autoGeneratedName);
        }
      } else {
        setGroupName("");
      }

      return newSelected;
    });
  };

  const filteredData = contacts.filter((contact) =>
    contact.name.toLowerCase().includes(searchText.toLowerCase()) ||
    contact.userId.toLowerCase().includes(searchText.toLowerCase())
  );

  const handleCreateGroup = async () => {
    if (!userId) {
      Alert.alert('ÈîôËØØ', 'Êó†Ê≥ïËé∑ÂèñÁî®Êà∑‰ø°ÊÅØ');
      return;
    }

    if (selectedFriends.length < 1) {
      Alert.alert('ÊèêÁ§∫', 'ËØ∑Ëá≥Â∞ëÈÄâÊã©‰∏Ä‰ΩçÂ•ΩÂèã');
      return;
    }

    if (!groupName.trim()) {
      Alert.alert('ÊèêÁ§∫', 'ËØ∑ËæìÂÖ•Áæ§ËÅäÂêçÁß∞');
      return;
    }

    try {
      setIsCreating(true);

      // Get selected friend details
      const selectedContacts = contacts.filter((contact) =>
        selectedFriends.includes(contact.id)
      );

      // Prepare group members array
      const groupMembers = [
        { user_id: userId, isadmin: 2 }, // Creator is admin
        ...selectedContacts.map(contact => ({
          user_id: contact.userId,
          isadmin: 1 // Regular members
        }))
      ];

      console.log('üìù Creating group with:', {
        name: groupName.trim(),
        user_id: userId,
        members: groupMembers.length
      });

      const response = await addGroup({
        name: groupName.trim(),
        user_id: userId,
        image: "",
        group: groupMembers
      });

      console.log('‚úÖ Group created:', response);

      if (response && !response.error) {
        const chatId = response.response;

        // Navigate to GroupScreen
        navigation.navigate("GroupScreen", {
          groupName: groupName.trim(),
          groupId: chatId,
          chatId: chatId,
        });
      } else {
        Alert.alert('ÈîôËØØ', response?.message || 'ÂàõÂª∫Áæ§ËÅäÂ§±Ë¥•');
      }
    } catch (error) {
      console.error('‚ùå Error creating group:', error);
      Alert.alert('ÈîôËØØ', 'ÂàõÂª∫Áæ§ËÅäÊó∂ÂèëÁîüÈîôËØØ');
    } finally {
      setIsCreating(false);
    }
  };

  if (isLoading) {
    return (
      <LinearGradient colors={colors.background.gradientYellow} style={styles.safeArea}>
        <SafeAreaView style={styles.container} edges={["top"]}>
          <View style={styles.header}>
            <TouchableOpacity
              style={styles.backButton}
              onPress={() => navigation.goBack()}
            >
              <Ionicons name="arrow-back" size={24} color={colors.text.primary} />
            </TouchableOpacity>
            <Text style={styles.headerTitle}>ÂèëËµ∑Áæ§ËÅä</Text>
            <View style={styles.placeholder} />
          </View>
          <View style={styles.loadingContainer}>
            <ActivityIndicator size="large" color={colors.text.primary} />
          </View>
        </SafeAreaView>
      </LinearGradient>
    );
  }

  return (
    <LinearGradient colors={colors.background.gradientYellow} style={styles.safeArea}>
      <SafeAreaView style={styles.container} edges={["top"]}>
        {/* Header */}
        <View style={styles.header}>
          <TouchableOpacity
            style={styles.backButton}
            onPress={() => navigation.goBack()}
          >
            <Ionicons name="arrow-back" size={24} color={colors.text.primary} />
          </TouchableOpacity>
          <Text style={styles.headerTitle}>ÂèëËµ∑Áæ§ËÅä</Text>
          <View style={styles.placeholder} />
        </View>

        {/* Search Bar */}
        <View style={styles.searchSection}>
          <View style={styles.searchBarContainer}>
            <Ionicons
              name="search"
              size={18}
              color={colors.text.lightGray}
              style={styles.searchIcon}
            />
            <TextInput
              placeholder="ÊêúÁ¥¢"
              placeholderTextColor={colors.text.lightGray}
              style={styles.searchInput}
              value={searchText}
              onChangeText={setSearchText}
            />
            {searchText.length > 0 && (
              <TouchableOpacity onPress={() => setSearchText("")}>
                <Ionicons name="close-circle" size={20} color={colors.text.grayLight} />
              </TouchableOpacity>
            )}
          </View>
        </View>

        {selectedFriends.length > 0 && (
          <>
            <View style={styles.selectedCountContainer}>
              <Text style={styles.selectedCountText}>
                ÈÄâÊã©‰∫Ü {selectedFriends.length} ‰∫∫
              </Text>
            </View>

            {/* Group Name Input */}
            <View style={styles.groupNameContainer}>
              <Text style={styles.groupNameLabel}>Áæ§ËÅäÂêçÁß∞</Text>
              <TextInput
                style={styles.groupNameInput}
                placeholder="ËØ∑ËæìÂÖ•Áæ§ËÅäÂêçÁß∞"
                placeholderTextColor={colors.text.lightGray}
                value={groupName}
                onChangeText={setGroupName}
                maxLength={50}
              />
            </View>
          </>
        )}

        {/* Friend List */}
        <FlatList
          data={filteredData}
          keyExtractor={(item) => item.id.toString()}
          renderItem={({ item }) => (
            <FriendItem
              item={item}
              isSelected={selectedFriends.includes(item.id)}
              onToggle={toggleFriend}
            />
          )}
          contentContainerStyle={styles.listContent}
          showsVerticalScrollIndicator={false}
        />

        {selectedFriends.length > 0 && (
          <View style={styles.createButtonContainer}>
            <TouchableOpacity
              style={[styles.createButton, isCreating && styles.createButtonDisabled]}
              onPress={handleCreateGroup}
              disabled={isCreating}
            >
              {isCreating ? (
                <ActivityIndicator size="small" color={colors.background.white} />
              ) : (
                <Text style={styles.createButtonText}>ÂèëËµ∑Áæ§ËÅä</Text>
              )}
            </TouchableOpacity>
          </View>
        )}
      </SafeAreaView>
    </LinearGradient>
  );
}

